Player::Player() {

	//initialize the sprite 
	PLAYER_SPRITE_RIGHT = new uint32_t[20 * 20];
	
	//load the sprite
	cimg_library::CImg<unsigned char> ps_right("assets/player_right_sprite.bmp");
	uint8_t* ps_right_pt = ps_right.data();

	//putting the image into the sprite variable
	for (int y = 0; y < 20; y++) {

		for (int x = 0; x < 20; x++) {

			// Get the RGB data
			uint8_t r = ps_right_pt[20 * y + x];
			uint8_t g = ps_right_pt[20 * y + x + 20 * 20];
			uint8_t b = ps_right_pt[20 * y + x + 2 * 20 * 20];
			uint32_t color = (r << 16) + (g << 8) + b;

			//it will adjust the background color of white into the appropreaite color
			if (color == 0xF7F7F7)
				PLAYER_SPRITE_RIGHT[20 * y + x] = LIGHT;
			else
				PLAYER_SPRITE_RIGHT[20 * y + x] = (r << 16) + (g << 8) + b;
		}
	}


	hitpoints = 100;
	attackDamage = 20;

	//by default, the player object will face to the right
	playerFacing = Direction::RIGHT;
	sprite = PLAYER_SPRITE_RIGHT;
	
}

void Player::setNewDirectionFacing(Direction newDir) {
	playerFacing = newDir;
	
	switch (newDir) {

		case Direction::UP:
			sprite = PLAYER_SPRITE_UP;
			break;

		case Direction::DOWN:
			sprite = PLAYER_SPRITE_DOWN;
			break;

		case Direction::LEFT:
			sprite = PLAYER_SPRITE_LEFT;
			break;

		case Direction::RIGHT:
			sprite = PLAYER_SPRITE_RIGHT;
			break;

		default:
			sprite = PLAYER_SPRITE_RIGHT;

	}
}

